static void update (struct TimeMock * const vThis)
{
    time_t now = time (ZERO);
    vThis->Tm = localtime (&now);
    strftime (vThis->Data, ONE_HUNDRED, "%Y-%m-%d %H:%M:%S", vThis->Tm);
}

struct TimeMock gTimeMock = { .Update = update };

